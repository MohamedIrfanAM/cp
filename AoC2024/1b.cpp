void solve()
{
  vector<int> a, b;
  int x, y;
  for (int i = 0; i < 1000; i++)
  {
    cin >> x >> y;
    a.pb(x);
    b.pb(y);
  }
  int ans = 0;
  unordered_map<int, int> h;
  for (int i = 0; i < a.size(); i++)
  {
    h[b[i]]++;
  }
  for (int i = 0; i < a.size(); i++)
  {
    ans += a[i] * h[a[i]];
  }
  cout << ans << "\n";
}

