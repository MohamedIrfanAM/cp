void solve() 
{
  int n; cin >> n;
  vector<pair<int,int>> v(n);
  for(int i = 0; i < n; i++) 
  {
    cin >> v[i].fi >> v[i].se;
  }
  sort(all(v));
  for(int i = 1; i < n; i++){
    if(v[i].fi >= v[i-1].fi && v[i].se >= v[i-1].se){
      continue;
    }
    else{
      cout << "Happy Alex\n";
      return;
    }
  }
  cout << "Poor Alex\n";
  
}

