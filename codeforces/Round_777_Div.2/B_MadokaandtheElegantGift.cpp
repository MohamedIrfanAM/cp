void solve() 
{
  int n,m;
  cin >> n >> m;
  vector<string> v(n);
  for(int i = 0; i < n; i++) 
  {
    cin >> v[i];
  }

  for(int i = 0; i < n-1; i++)
  {
    for(int j = 0; j < m-1; j++)
    {
      int count = v[i][j]+v[i+1][j]+v[i+1][j+1]+v[i][j+1]-(4*'0');
      if(count == 3){
        cout << "NO\n";
        return;
      }
    }
  }
  cout << "YES\n";
}

