int32_t main()
{
ios::sync_with_stdio(0);cin.tie(0);cout.tie(0);

int tst;
cin >> tst;
while(tst--)
{
  int n;
  cin >> n;

  vector<int> c;

  for(int i = 1; i <= n; i++)
  {
    for(int j = 1; j <= n; j++)
    {
      vector<int> h(n+1,0);
      int cost = 0;
      int m = i*j;
      for(int k = n; k >= 1; k--)
      {
        if(k == j ) continue;
        if(k > m) break;
        int l = min(m/k,n);
        while(h[l] == 1 || l == i)
        {
          l--;
        }
        if(l == 0 ) break;
        cost += l*k;
        h[l] = 1;
      }
      c.pb(cost);
    }
  }
  cout << *max_element(all(c)) << endl;

}

return 0;
}
